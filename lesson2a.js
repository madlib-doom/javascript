// Javascript object data types

let person={
    name:"Joseph william",
    age:53,
    isTeacher:true

}

console.log("The details of the person are:",person)

// we use the dot separator to access specific values of an object.
console.log("The name of the person is:",person.name)
console.log("The age of the person is:",person.age)
console.log("Is the person  a TEACHER:",person.isTeacher)

